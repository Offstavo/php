<?php
    // switch
    $num = 20;
    switch($num){
        case 10:
            echo("$num is equals to 10");
            break;
        case 20:
            echo("$num is equals to 20");
            break;
        case 30:
            echo("$num is equals to 30");
            break;
        default:
            echo("$num is not equals to 10,20 or 30");
    }
    // code worked as expected
?>